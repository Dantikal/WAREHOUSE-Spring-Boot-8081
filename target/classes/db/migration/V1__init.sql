-- Warehouse
CREATE TABLE IF NOT EXISTS warehouse (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(255) NOT NULL,
    address     VARCHAR(500),
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Product
CREATE TABLE IF NOT EXISTS product (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    barcode           VARCHAR(100),
    name              VARCHAR(255) NOT NULL,
    pieces_per_box    INT DEFAULT 1,
    expiration_date   DATE,
    batch_number      VARCHAR(100),
    factory_price     DECIMAL(15,2) NOT NULL DEFAULT 0,
    driver_price      DECIMAL(15,2) NOT NULL DEFAULT 0,
    status            VARCHAR(50) DEFAULT 'ACTIVE',
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Driver
CREATE TABLE IF NOT EXISTS driver (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name       VARCHAR(255) NOT NULL,
    phone           VARCHAR(50),
    vehicle_number  VARCHAR(50),
    debt            DECIMAL(15,2) DEFAULT 0,
    status          VARCHAR(50) DEFAULT 'ACTIVE'
);

-- Inventory
CREATE TABLE IF NOT EXISTS inventory (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id      BIGINT NOT NULL REFERENCES warehouse(id),
    product_id        BIGINT NOT NULL REFERENCES product(id),
    quantity_boxes    INT DEFAULT 0,
    quantity_pieces   INT DEFAULT 0,
    updated_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Inventory Movement
CREATE TABLE IF NOT EXISTS inventory_movement (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id    BIGINT NOT NULL REFERENCES warehouse(id),
    product_id      BIGINT NOT NULL REFERENCES product(id),
    movement_type   VARCHAR(50) NOT NULL,
    quantity_boxes  INT DEFAULT 0,
    quantity_pieces INT DEFAULT 0,
    reference_id    BIGINT,
    comment         VARCHAR(500),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Factory Delivery
CREATE TABLE IF NOT EXISTS factory_delivery (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id     BIGINT NOT NULL REFERENCES warehouse(id),
    delivery_number  VARCHAR(100),
    delivery_date    DATE,
    total_amount     DECIMAL(15,2) DEFAULT 0,
    status           VARCHAR(50) DEFAULT 'PENDING'
);

-- Factory Delivery Item
CREATE TABLE IF NOT EXISTS factory_delivery_item (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    delivery_id       BIGINT NOT NULL REFERENCES factory_delivery(id),
    product_id        BIGINT NOT NULL REFERENCES product(id),
    expected_quantity INT DEFAULT 0,
    actual_quantity   INT DEFAULT 0,
    shortage          INT DEFAULT 0,
    surplus           INT DEFAULT 0,
    defective         INT DEFAULT 0
);

-- Driver Order
CREATE TABLE IF NOT EXISTS driver_order (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    driver_id     BIGINT NOT NULL REFERENCES driver(id),
    warehouse_id  BIGINT NOT NULL REFERENCES warehouse(id),
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    total_amount  DECIMAL(15,2) DEFAULT 0,
    status        VARCHAR(50) DEFAULT 'NEW'
);

-- Driver Order Item
CREATE TABLE IF NOT EXISTS driver_order_item (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id    BIGINT NOT NULL REFERENCES driver_order(id),
    product_id  BIGINT NOT NULL REFERENCES product(id),
    quantity    INT DEFAULT 0
);

-- Dispatch
CREATE TABLE IF NOT EXISTS dispatch (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id    BIGINT NOT NULL REFERENCES warehouse(id),
    driver_id       BIGINT NOT NULL REFERENCES driver(id),
    invoice_number  VARCHAR(100),
    total_amount    DECIMAL(15,2) DEFAULT 0,
    dispatch_date   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status          VARCHAR(50) DEFAULT 'CREATED'
);

-- Dispatch Item
CREATE TABLE IF NOT EXISTS dispatch_item (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dispatch_id      BIGINT NOT NULL REFERENCES dispatch(id),
    product_id       BIGINT NOT NULL REFERENCES product(id),
    quantity_boxes   INT DEFAULT 0,
    quantity_pieces  INT DEFAULT 0,
    price            DECIMAL(15,2) DEFAULT 0
);

-- Cashbox
CREATE TABLE IF NOT EXISTS cashbox (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id    BIGINT NOT NULL REFERENCES warehouse(id),
    current_balance DECIMAL(15,2) DEFAULT 0
);

-- Cashbox Transaction
CREATE TABLE IF NOT EXISTS cashbox_transaction (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id      BIGINT NOT NULL REFERENCES warehouse(id),
    transaction_type  VARCHAR(50) NOT NULL,
    amount            DECIMAL(15,2) NOT NULL,
    payment_method    VARCHAR(50),
    comment           VARCHAR(500),
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Driver Payment
CREATE TABLE IF NOT EXISTS driver_payment (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    driver_id       BIGINT NOT NULL REFERENCES driver(id),
    warehouse_id    BIGINT NOT NULL REFERENCES warehouse(id),
    amount          DECIMAL(15,2) NOT NULL,
    payment_method  VARCHAR(50),
    comment         VARCHAR(500),
    payment_date    TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Return Operation
CREATE TABLE IF NOT EXISTS return_operation (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id    BIGINT NOT NULL REFERENCES warehouse(id),
    driver_id       BIGINT NOT NULL REFERENCES driver(id),
    return_reason   VARCHAR(50),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Factory Payment
CREATE TABLE IF NOT EXISTS factory_payment (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id  BIGINT NOT NULL REFERENCES warehouse(id),
    amount        DECIMAL(15,2) NOT NULL,
    payment_date  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    comment       VARCHAR(500)
);

-- Debt
CREATE TABLE IF NOT EXISTS debt (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    warehouse_id  BIGINT NOT NULL REFERENCES warehouse(id),
    driver_id     BIGINT REFERENCES driver(id),
    debt_type     VARCHAR(50) NOT NULL,
    amount        DECIMAL(15,2) DEFAULT 0
);

